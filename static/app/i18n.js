const LANG_KEY = 'gemini2api_lang';
let currentLang = localStorage.getItem(LANG_KEY) || 'zh-CN';

export function t(key) {
    const lang = translations[currentLang] || translations['zh-CN'];
    return lang[key] || translations['zh-CN'][key] || key;
}

export function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    localStorage.setItem(LANG_KEY, lang);
    updatePageLanguage();
}

export function getCurrentLanguage() {
    return currentLang;
}

export function updatePageLanguage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        el.title = t(key);
    });
}

export function initI18n() {
    updatePageLanguage();
    let updating = false;
    const observer = new MutationObserver((mutations) => {
        if (updating) return;
        let hasNewElements = false;
        for (const m of mutations) {
            for (const node of m.addedNodes) {
                if (node.nodeType === 1) { hasNewElements = true; break; }
            }
            if (hasNewElements) break;
        }
        if (hasNewElements) {
            updating = true;
            updatePageLanguage();
            updating = false;
        }
    });
    observer.observe(document.body, { childList: true, subtree: true });
}
